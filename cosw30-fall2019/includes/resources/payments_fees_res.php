                By law, we must collect applicable sales tax for orders shipping to states where we have retail stores. Shipping fees may also be subject to tax in certain states. The sales tax for an order will be displayed
                in the Order Summary once the shipping address has been entered.<br>

                Sales tax does not apply to purchases of Donations, Gift Cards and eGift Cards.<br><br>

                Tax Exemption<br><br><br>
                For those who qualify for a tax exemption, please submit your request for exemption by faxing your tax certificate, resale certificate, or other required documentation to (858) 784-3489. On a cover sheet, be sure to include your order number. A Tax Specialist will review your certificate once it has been received. We will notify you by email within five (5) business days if your request has been approved.<br>

                Your order will initially be taxed. Once we've received your request and it has been approved, we will process a refund back to the original form of payment for the applicable amount. An automated email will be sent as confirmation that the refund has been processed. You may also log in to your account to view the adjustment on the Order Details page. Please allow up to 10 business days for the refund to appear on your banking statement.</p>

